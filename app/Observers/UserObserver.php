    public function deleted(User $user)
    {
            $friendships = Friendship::where('requester', '=', $user['id'])->get()->toArray();
            foreach($friendships as $friend){
                $friend->delete();
            }

            $friendships = Friendship::where('userRequested', '=', $user['id'])->get()->toArray();
            foreach($friendships as $friend){
                $friend->delete();
            }
    }

